
public class Department {
	private String departmentId;
	private String name;
	public String getDepartmentId() {
		return departmentId;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	public Department(String name) {
		super();
		this.name = name;
	}
	
	
}
